<h2><b><u>General Rules</u></b></h2>
<br>
<p>
 <br> >>Consumption of Alcoholic Beverages are strictly prohibited inside the Hall premises. Also no Hall residents are allowed to enter the Hall in an intoxicated State. Any violation of this rule calls for a fine of Rs. 2500/-
 <br> >>Smoking Cigarettes in public inside the Hall is strictly prohibited. A fine of Rs. 1,000/- shall be imposed on defaulters.
 <br> >>Also owing to the placement preparations and academic load, no resident is allowed to create a nuisance, play loud music or make loud sounds from 10:00 pm to 8:00 am. Any violation of this calls for first time warning and next time caretaker will take appropriate action. Hall-4 Caretaker is allowed to seize the music system from that room.
  <br> >> Any tampering or destruction of Hall Property (in intoxicated State or otherwise), shall call for an additional fine equal to the Cost of the property.
  <br> >> Bringing and/or consuming any eatable or beverage inside the Reading Room, Common WiFi Room and TT Room is strictly not allowed. Anyone found violating this rule will be fined Rs. 250.
  <br> >>Sleeping in the Reading Room or Common WiFi Room is not allowed and any violation will attract a fine of Rs. 100.
  <br> >> Damaging the Hall property in any manner is not allowed. Appropriate monetary/disciplinary action will be taken against those who damage hall property.
   <br> >> Parking bicycles at any place other than the Cycle Stands and places where cycle racks are placed is prohibited. Randomly parked Cycles, especially those parked in and around wings, will be seized by Hall Office and will only be released after a fine of Rs. 100 and a written apology letter.
   <br> >> Leaving the lights and fan 'on' when not in the room attracts a penalty of Rs. 100 on each occupant of the room.
    <br> >>Anyone found jumping the hall boundary will be fined Rs.500/-.
     <br> >>Anyone found damaging and changing direction of CCTV Cameras will be fined Rs.500/-.
    <br> >> Any of the Hostel Committee Can't take meeting with the residents without the Consent of Hall Executive Committee Convener.
    <br> >> Strict action will be taken against anyone found indulging in any acts of misconduct and/or disturbing the harmony of the hall.
    <br> >> Lastly, to be vigilant about the misuse of the Scarce resources of water and electricity.

</p>
