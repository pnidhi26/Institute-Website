           <h2><b><u>Rules for all Residents of Hall-4</u></b></h2>
                    <ul>

                    <li> On arrival a student will report to the Caretaker or any other official of the hostel authorized by the Warden and will take possession of the room after signing the inventory of the furniture,<br>electrical and other items in the room.</li>

                  <li> At the end of each semester a student shall vacate his/her room and hand over the charge of the room including all items on the inventory to the Caretaker-or-any other official of the hostel
                  authorized by the Warden. Any student desirous of retaining his room during the vacation must seek prior permission with valid reason to his Warden.</li>

                <li> Students are not allowed to bring additional furniture other than provided by the institute.</li>

              <li> Use of narcotics, consumption of alcoholic beverages and gambling are strictly prohibited.</li>

            <li>To maintain the privacy of roommate, guests are not allowed inside any hostel rooms, students should meet the guests only in the visitor's room.</li>

          <li>For the guest visiting students undergoing medical treatment, they might be permitted inside the room for limited time with proper permission.</li>

        <li>It is mandatory and ordinarily sufficient for a resident to inform the caretaker/Warden about any guest(s) staying overnight with him, with express provision that they are empowered to deny permission if the situation so warrants. For guests staying up to two days, prior permission would have to be obtained from the Warden only. For longer periods permission is to be sought from the Dean (Students).</li>

      <li>All hostel residents must take their food in the mess only.</li>

    <li>The institute furniture allotted to the students for use during their stay should be used carefully. Any damage done to the properties intentionally/accidentally will lead to severe punishment along with monetary fine.</li>

  <li>Inter-change/clubbing/shifting of furniture, electrical and any other item with one room to another or Common Room is prohibited.</li>

<li>Consumption of liquor in the hostel premise or coming to the hostel in an inebriated state is strictly prohibited.</li>

<li>Students are not allowed to use high-wattage electrical appliances e.g. cooler, immersion rods, room heaters or for ironing of clothes.</li>


<li>Inside the hostel rooms use of cooking gas, and kerosene Stove music system, etc. is not allowed.</li>


<li>Students are not allowed to leave the campus without prior permission of the warden. If
'student is required to leave the campus for a night he/she has to fill up a leave form which
is available in hostel office. Students can also fill the leave form online at Hall-4 website.</li>

<li>All the residents are suggested to carry their Institute ID Card with them always. Security Guard/ Office Staff deputed in Hall-4 may ask it to check the same before entrance in Hall-4.</li>

<li>All residents will have to give his/her attendance every day between 7PM to 10PM in the
Warden Office. Failing to which a fine of R50/day will be imposed on the student, and/or strict disciplinary action will be taken.</li>

<li>Playing games e.g. Football, Cricket etc. in the gardens of Hall-4 is strictly prohibited.</li>

<li>Students should maintain peace and tranquility in the hostel premises. After 22:00 hours, the students should not make any noise, violation of which can lead to disciplinary action.</li>

<li>Always use dustbin for throwing waste. Under no circumstances may anything be thrown out of the window/door.</li>

<li>A resident should strictly follow the mess timings as decided.</li>

<li>A student has to sign in the register kept for the purpose if he/she is going outside from the hostel after 10 P.M.</li>

<li>Hostel Rooms are allotted on 'first-come-first-serve' basis. Hostel Room once allotted is not allowed to change.</li>

<li>All residents shall comply with the rules, supplementary rules and by-laws as may be framed from time to time. Ignorance of any of these shall NOT be accepted as an excuse for its non-observance on the part of the resident. For any violation of the rules and for any act of indiscipline, the wardens shall take appropriate action.</li>

<li>Repeated violation of any of the rules will result in severe disciplinary action and case Will be forwarded to SSAC.</li>

<li>Fixing posters and pictures or Writing on the common walls and disfiguring the walls of the hall are strictly prohibited. Anybody found violating these rules may be penalized by the Hall Wardens.</li>

<li>To protect the privacy of the residents, visitors of the opposite sex are strictly banned in the rooms and blocks of each other between 9:00 P.M. to 9:00 A.M.</li>

<li>Girls have to sign in the register kept for the purpose if they are coming to Hall-4 premises for any purpose.</li>

<li>Walking in the campus during night time/dim light conditions is strictly restricted and institute will not be responsible for those students who violate the above rule.</li>

<li>Last but not the least, indiscipline of any kind which is not mentioned above will not be excused.</li>


</ul>


